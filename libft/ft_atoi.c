int		ft_atoi(char const *str)
{
	int i;
	int j;
	int neg;

	i = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	j = i;
	if (str[i] == '+')
		i++;
	if (str[i] == '-' && j == i)
	{
		neg = -1;
		i++;
	}
	j = 0;
	while (str[i] < 58 && str[i] > 47)
	{
		j = j * 10 + (str[i] - 48);
		i++;
	}
	j *= neg;
	return (j);
}
