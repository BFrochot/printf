#include "libft.h"

static int	ft_cnt_parts(const char *s, char c)
{
	int		nw;
	int		i;

	i = 0;
	nw = 0;
	while (s[i++])
		if ((s[i - 1] == c || nw == 0) && s[i] != c && s[i] != '\0')
			nw++;
	return (nw);
}

static int	ft_wlen(const char *s, char c)
{
	int		len;

	len = 0;
	while (*s != c && *s != '\0')
	{
		len++;
		s++;
	}
	return (len);
}

char		**ft_strsplit(char const *s, char c)
{
	char	**t;
	int		nw;
	int		index;

	if (s == NULL)
		return (NULL);
	index = 0;
	nw = ft_cnt_parts((const char *)s, c);
	t = (char **)malloc(sizeof(*t) * (ft_cnt_parts((const char *)s, c) + 1));
	if (t == NULL)
		return (NULL);
	while (nw--)
	{
		while (*s == c && *s != '\0')
			s++;
		t[index] = ft_strsub((const char *)s, 0, ft_wlen((const char *)s, c));
		if (t[index] == NULL)
			return (NULL);
		s = s + ft_wlen(s, c);
		index++;
	}
	t[index] = NULL;
	return (t);
}
